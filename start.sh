#!/bin/sh
# Применяем миграции автоматически при каждом старте.
#
# ВАЖНО: это безопасно только для одного инстанса (single instance), что и
# есть наш случай на бесплатном тарифе Render. Если в будущем перейдёте на
# масштабирование с несколькими инстансами (Standard/Pro тариф Render с
# несколькими репликами), миграции нужно будет вернуть в отдельный шаг
# деплоя, а не запускать в CMD каждого инстанса — иначе несколько инстансов
# могут одновременно попытаться применить одну и ту же миграцию.
#
# alembic upgrade head безопасен для повторного запуска: если миграции уже
# применены, команда просто ничего не делает (проверяет alembic_version).
set -e

echo "Applying database migrations..."
alembic upgrade head
echo "Migrations applied. Starting server..."

exec uvicorn app.main:app --host 0.0.0.0 --port "${PORT:-8000}"
